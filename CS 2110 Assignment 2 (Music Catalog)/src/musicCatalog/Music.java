package musicCatalog;

public class Music {
	
	private int songId;	
	private String title;
	private String genre;
	private String comment;
	private double duration;
	
	//Accessor Comments
	public int getId(){
		return songId;
	}
	public String getTitle(){
		return title;
	}
	public String getGenre(){
		return genre;
	}
	public String getComment(){
		return comment;
	}
	public double getDuration(){
		return duration;
	}
	
	

}
